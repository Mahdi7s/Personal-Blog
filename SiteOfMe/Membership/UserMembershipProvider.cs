        public override bool ValidateUser(string username, string password)
        {
            Debug.Assert(!string.IsNullOrEmpty(username) || !string.IsNullOrEmpty(password));
            var user = _unitOfWork.UserRep.GetByUserName(username);
            return (user != null && Hasher.VerifyPass(password, user.Password));
        }

        public override bool ChangePassword(string username, string oldPassword, string newPassword)
        {
            var user = _unitOfWork.UserRep.GetByUserName(username);
            Debug.Assert(user != null);

            if(Hasher.VerifyPass(oldPassword, user.Password))
            {
                user.Password = Hasher.HashOf(newPassword);
                _unitOfWork.SaveChanges();
                return true;
            }
            return false;
        }
